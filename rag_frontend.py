# ui.py
import streamlit as st
import uuid # -> Universally Unique Identifier. it can generate id's.
from langchain_core.messages import HumanMessage, AIMessage, ToolMessage
from rag_backend import chatbot, retrieve_all_threads

# st.session_state -> Dict -> it stores conversation untill the page was not refresh it.

# ==================================== Utility Function ====================================

def generate_thread_id():
    return str(uuid.uuid4())              # it will generate new id (automatically)

def add_thread(thread_id):
    if thread_id not in st.session_state['chat_threads']:
        st.session_state['chat_threads'].append(thread_id)

def reset_chat():
    thread_id = generate_thread_id()
    st.session_state['thread_id'] = thread_id
    add_thread(thread_id)
    st.session_state['message_history'] = []
    

def load_conversation(thread_id):
    state = chatbot.get_state(config={'configurable' : {'thread_id' : thread_id}})
    return state.values.get('messages', [])



# ==================================== SESSION SETUP ====================================

if "message_history" not in st.session_state:    # if not present message_history.
    st.session_state['message_history'] = []     # then it creates new message_history list.

if "thread_id" not in st.session_state:                        
    st.session_state["thread_id"] = generate_thread_id()

if 'chat_threads' not in st.session_state:       # if not present chat_threads
    st.session_state['chat_threads'] = retrieve_all_threads()

add_thread(st.session_state['thread_id'])

# ==================================== PAGE CONFIG UI ====================================
st.set_page_config(page_title="Defence Agentic RAG", page_icon="ğŸ›¡ï¸", layout="wide")

# ==================================== SIDEBAR UI ====================================

st.sidebar.title("Chat History")

if st.sidebar.button("âœğŸ» New Chat"):
    reset_chat()

st.sidebar.divider()

st.sidebar.header("ğŸ’¬ Your Chats")

for thread_id in reversed(st.session_state['chat_threads']):
    if st.sidebar.button(thread_id, key=thread_id):                 # if thread_id button is click
        st.session_state['thread_id'] = thread_id         # save in session_state[thread_id]
        
        messages = load_conversation(thread_id)           # and load_conversation 

        history = []

        for msg in messages:
            if isinstance(msg, HumanMessage):
                history.append({'role' : 'user', 'content' : msg.content})

            elif isinstance(msg, AIMessage):
                history.append({'role' : 'assistant', 'content' : msg.content})

        st.session_state['message_history'] = history
       

# ==================================== MAIN CHAT UI ====================================

st.title("ğŸ›¡ Defence RAG Agent Chatbot")

# Display chat history
for message in st.session_state['message_history']:
    with st.chat_message(message['role']):
        st.markdown(message['content'])

# User Input 
user_input = st.chat_input("Ask about Air Missile Defense, Drones, B-2 Spirit systems & defence Topicsâ€¦")

if user_input:
   # Show user message
    st.session_state['message_history'].append({'role' : 'user', 'content' : user_input})
    with st.chat_message("user"):
        st.text(user_input)
    
    CONFIG = {"configurable": {"thread_id": st.session_state["thread_id"]},
              "metadata": {"thread_id": st.session_state["thread_id"]},
              "run_name" : "chat_turn"}

    # Assistant streaming block
    with st.chat_message("assistant"):
        # Use a mutable holder so the generator can set/modify it
        status_holder = {"box": None}
        

        def ai_only_stream():
            for message_chunk, metadata in chatbot.stream(
                {"messages": [HumanMessage(content=user_input)]},
                config=CONFIG,
                stream_mode="messages",
            ):
                # Lazily create & update the SAME status container when any tool runs
                if isinstance(message_chunk, ToolMessage):
                    tool_name = getattr(message_chunk, "name", "tool")
                    if status_holder["box"] is None:
                        status_holder["box"] = st.status(
                            f"ğŸ”§ Using `{tool_name}` â€¦", expanded=True
                        )
                    else:
                        status_holder["box"].update(
                            label=f"ğŸ”§ Using `{tool_name}` â€¦",
                            state="running",
                            expanded=True,
                        )

                # Stream ONLY assistant tokens
                if isinstance(message_chunk, AIMessage):
                    yield message_chunk.content

        ai_message = st.write_stream(ai_only_stream())

        # Finalize only if a tool was actually used
        if status_holder["box"] is not None:
            status_holder["box"].update(
                label="âœ… Tool finished", state="complete", expanded=False
            )
    
    # Save assistant message
    st.session_state["message_history"].append(
        {"role": "assistant", "content": ai_message}
    )